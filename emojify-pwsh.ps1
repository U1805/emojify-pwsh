function Convert-UnicodeSequence {
    param (
        [Parameter(Mandatory=$true)]
        [string]$Sequence
    )
    
    $Sequence -split ' ' | ForEach-Object {
        [char]::ConvertFromUtf32([Convert]::ToInt32($_, 16))
    } | Join-String
}

