        function Emojify-Token {
            param ([string]$token)
            if ($script:emojis.ContainsKey($token)) {
                return $script:emojis[$token]
            }
            return $token
        }

        function Emojify-Line {
            param ([string]$line)
            $current_token = ""
            $emojified_line = ""
            
            for ($i = 0; $i -lt $line.Length; $i++) {
                $current_char = $line[$i]
                
                if ($current_token -eq "") {
                    if ($current_char -eq ':') {
                        $current_token += $current_char
                    }
                    else {
                        $emojified_line += $current_char
                    }
                }
                else {
                    if ($current_char -eq ':') {
                        $emojified_token = Emojify-Token "$current_token$current_char"
                        if ($emojified_token -eq "$current_token$current_char") {
                            $emojified_line += $current_token
                            $current_token = $current_char
                        }
                        else {
                            $emojified_line += $emojified_token
                            $current_token = ""
                        }
                    }
                    elseif ($current_char -match '[a-zA-Z0-9_+-]') {
                        $current_token += $current_char
                    }
                    else {
                        $current_token += $current_char
                        $emojified_line += $current_token
                        $current_token = ""
                    }
                }
            }
            $emojified_line += $current_token
            return $emojified_line
        }

        function Show-Help {
            param ([switch]$List)
            Write-Host "emojify - emoji on the command line 😱"
            Write-Host ""
            Write-Host "USAGE: Emojify [-Help] [-List] [-Version] [TEXT]"
            
            if ($List) {
                Write-Host "Supported emojis:"
                foreach ($key in $script:emojis.Keys) {
                    Write-Host "$key $($script:emojis[$key])"
                }
            }
        }
    }

    process {
        if ($Help) { 
            Show-Help
            return
        }
        if ($List) { 
            Show-Help -List
            return
        }
        if ($Version) { 
            Write-Host $emojify_version
            return
        }
        if ($InputObject) {
            Write-Output (Emojify-Line $InputObject)
        }
    }
